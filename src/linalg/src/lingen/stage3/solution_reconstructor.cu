bool SolutionReconstructor::is_zero_vector(const uint64_t* d_ptr, cudaStream_t stream) {
    size_t total_bytes = nrows_aligned_ * ((n_block_ + 63) / 64) * sizeof(uint64_t);
    CHECK_CUDA_REC(cudaMemcpyAsync(h_chk_buf_.data(), d_ptr, total_bytes, cudaMemcpyDeviceToHost, stream));
    CHECK_CUDA_REC(cudaStreamSynchronize(stream));
    for (size_t i = 0; i < h_chk_buf_.size(); ++i) if (h_chk_buf_[i] != 0) return false;
    return true;
}

bool SolutionReconstructor::strip_valuation(uint64_t* d_w, uint64_t* d_tmp, int max_steps, cudaStream_t stream) {
    size_t total_bytes = nrows_aligned_ * ((n_block_ + 63) / 64) * sizeof(uint64_t);
    CHECK_CUDA_REC(cudaMemsetAsync(d_tmp, 0, total_bytes, stream));
    op_.mul(d_tmp, d_w, stream);
    if (is_zero_vector(d_tmp, stream)) return true;
    CHECK_CUDA_REC(cudaMemcpyAsync(d_w, d_tmp, total_bytes, cudaMemcpyDeviceToDevice, stream));
    for(int step = 0; step < max_steps; ++step) {
        CHECK_CUDA_REC(cudaMemsetAsync(d_tmp, 0, total_bytes, stream));
	op_.mul(d_tmp, d_w, stream);
        if (is_zero_vector(d_tmp, stream)) return true;
        CHECK_CUDA_REC(cudaMemcpyAsync(d_w, d_tmp, total_bytes, cudaMemcpyDeviceToDevice, stream));
    }
    return false;
}

uint64_t SolutionReconstructor::hash_device_vector(const uint64_t* d_ptr, cudaStream_t stream) {
    // Note: Hashing strictly nrows_ for Oracle compatibility, even if underlying buffer is aligned
    size_t total_bytes = nrows_ * ((n_block_ + 63) / 64) * sizeof(uint64_t);
    CHECK_CUDA_REC(cudaMemcpyAsync(h_chk_buf_.data(), d_ptr, total_bytes, cudaMemcpyDeviceToHost, stream));
    CHECK_CUDA_REC(cudaStreamSynchronize(stream));
    return fnv1a_hash_uint64(h_chk_buf_);
}

void SolutionReconstructor::VerifyAgainstOracle(int candidate_col_idx, 
                                                const uint64_t* d_w_horner, 
                                                const uint64_t* d_w_final,
                                                cudaStream_t stream)
{
    auto it = oracle_data_.find(candidate_col_idx);
    if (it == oracle_data_.end()) {
        if (batch_mode_) {
            LOG(LOG_DEBUG_3) << "[SolutionReconstructor] Oracle entry missing for " << candidate_col_idx 
                             << ". Likely over-production in batch mode. Skipping verification.";
            return;
        }
        LOG(LOG_ERROR_CRITICAL) << "[SolutionReconstructor] Oracle Verification FAILED: Candidate " << candidate_col_idx << " not in oracle.";
        throw std::runtime_error("Oracle verification failed: candidate not in oracle");
    }
    const OracleEntry& ref = it->second;
    if (d_w_horner) {
        uint64_t h = hash_device_vector(d_w_horner, stream);
        if (h != ref.hash_horner) {
            LOG(LOG_ERROR_CRITICAL) << "[SolutionReconstructor] Oracle Mismatch (Horner) for " << candidate_col_idx
                                    << ": 0x" << std::hex << h << " != 0x" << ref.hash_horner << std::dec;
            throw std::runtime_error("Oracle hash mismatch (Horner)");
        }
    }
    if (d_w_final) {
        uint64_t f = hash_device_vector(d_w_final, stream);
        if (f != ref.hash_final) {
            LOG(LOG_ERROR_CRITICAL) << "[SolutionReconstructor] Oracle Mismatch (Final) for " << candidate_col_idx
                                    << ": 0x" << std::hex << f << " != 0x" << ref.hash_final << std::dec;
            throw std::runtime_error("Oracle hash mismatch (Final)");
        }
    }
    LOG(LOG_DEBUG_2) << "[SolutionReconstructor] Verified Candidate " << candidate_col_idx;
}

