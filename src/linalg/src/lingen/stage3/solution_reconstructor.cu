static std::vector<uint64_t> extract_coeff_vector(
    const std::vector<uint64_t>& hPi, 
    int M, 
    int n_block, 
    int degree_k, 
    int row_idx
) {
    size_t words_per_row_M = (M + 63) / 64;
    size_t mat_stride = M * words_per_row_M;
    const uint64_t* mat_ptr = hPi.data() + degree_k * mat_stride;
    const uint64_t* row_ptr = mat_ptr + row_idx * words_per_row_M;
    std::vector<uint64_t> u_vec((n_block + 63) / 64, 0);
    int words_needed = (n_block + 63) / 64;
    for(int w=0; w<words_needed; ++w) u_vec[w] = row_ptr[w];
    return u_vec;
}

