// Batch Execution Logic
// -----------------------------------------------------------------------------
bool SolutionReconstructor::FindNextBatch(std::vector<uint64_t>& out_solution, cudaStream_t stream) {
    if (!ptr_hPi_) return false;

    // 1. Drain Buffer
    if (!solution_buffer_.empty()) {
        out_solution = solution_buffer_.front();
        solution_buffer_.pop_front();
        return true;
    }

    size_t words_per_row = (n_block_ + 63) / 64;
    size_t total_bytes = nrows_aligned_ * words_per_row * sizeof(uint64_t);

    // 2. Process Next Batch
    while (!batch_queue_.empty()) {
        BatchCandidate batch = batch_queue_.front();
        batch_queue_.pop_front();

        LOG(LOG_DEBUG_1) << "[SolutionReconstructor] Processing Batch of size " << batch.candidates.size() 
                         << ", MaxDeg=" << batch.max_degree;

        // Reset
        CHECK_CUDA_REC(cudaMemsetAsync(d_W_batch_, 0, total_bytes, stream));
    
        std::vector<uint64_t> h_U(n_block_ * words_per_row);

        // Determine if graph capture is worthwhile for this batch
#ifdef BW_ENABLE_CUDA_GRAPHS
#if CUDART_VERSION >= 10010
        bool use_graph_this_batch = config_.use_cuda_graph
                                 && (batch.max_degree >= config_.graph_min_iterations)
                                 && !config_.oracle_verification;
#endif
#endif

        // --- Parallel Horner Loop ---
        for (int k = 0; k <= batch.max_degree; ++k) {
	    if (!(k % 100) && k > 0)
	      LOG(LOG_DEBUG_2) << "[SolutionReconstructor] Horner Loop Degree = " << k << " (" << 100.0*(float)k/(float)batch.max_degree << "%)";

            // A. Shift
            if (k > 0) {
                CHECK_CUDA_REC(cudaMemsetAsync(d_Tmp_, 0, total_bytes, stream));
                op_.mul(d_Tmp_, d_W_batch_, stream);
                CHECK_CUDA_REC(cudaMemcpyAsync(d_W_batch_, d_Tmp_, total_bytes, cudaMemcpyDeviceToDevice, stream));
            }

            // B. Project
            construct_batch_matrix(h_U, n_block_, *ptr_hPi_, M_, k, batch.candidates);
            CHECK_CUDA_REC(cudaMemcpyAsync(d_U_small_, h_U.data(), h_U.size()*sizeof(uint64_t), cudaMemcpyHostToDevice, stream));

            int num_blocks = nrows_aligned_ / n_block_;
            int u_words = h_U.size();
            int threads = 256;
            size_t total_w = nrows_aligned_ * words_per_row;

#ifdef BW_ENABLE_CUDA_GRAPHS
#if CUDART_VERSION >= 10010
            if (use_graph_this_batch) {
                if (!proj_graph_captured_) {
                    // First iteration: capture the 3-kernel projection chain
                    cudaError_t cap_err = cudaStreamBeginCapture(stream,
                                                                  cudaStreamCaptureModeThreadLocal);
                    if (cap_err != cudaSuccess) {
                        LOG(LOG_WARNING) << "[SolutionReconstructor] Graph capture begin failed: "
                                         << cudaGetErrorString(cap_err) << ". Falling back to direct launches.";
                        use_graph_this_batch = false;
                    } else {
                        int blocks_rep = (u_words + threads - 1) / threads;
                        k_replicate_matrix<<<blocks_rep, threads, 0, stream>>>(
                            d_U_expanded_, d_U_small_, num_blocks, u_words);

                        matmul_gf2_gpu(n_block_, d_Z_, d_U_expanded_, d_MatMul_Res_, num_blocks, stream);

                        int blocks_xor = (total_w + threads - 1) / threads;
                        k_xor_accumulate<<<blocks_xor, threads, 0, stream>>>(
                            d_W_batch_, d_MatMul_Res_, total_w);

                        cudaGraph_t graph;
                        cap_err = cudaStreamEndCapture(stream, &graph);
                        if (cap_err != cudaSuccess || graph == nullptr) {
                            LOG(LOG_WARNING) << "[SolutionReconstructor] Graph capture failed: "
                                             << cudaGetErrorString(cap_err) << ". Falling back to direct launches.";
                            use_graph_this_batch = false;
                        } else {
                            cap_err = cudaGraphInstantiate(&proj_graph_exec_, graph, nullptr, nullptr, 0);
                            cudaGraphDestroy(graph);
                            if (cap_err != cudaSuccess) {
                                LOG(LOG_WARNING) << "[SolutionReconstructor] Graph instantiation failed: "
                                                 << cudaGetErrorString(cap_err) << ". Falling back to direct launches.";
                                proj_graph_exec_ = nullptr;
                                use_graph_this_batch = false;
                            } else {
                                proj_graph_captured_ = true;
                                LOG(LOG_DEBUG_2) << "[SolutionReconstructor] CUDA graph captured for projection chain ("
                                                 << "3 nodes, will replay for remaining " << batch.max_degree << " iterations)";
                            }
                        }
                    }
                }

                if (proj_graph_captured_) {
                    // Replay: H→D upload of d_U_small_ already completed above (async on stream).
                    // The graph reads from d_U_small_ which now contains the new data.
                    CHECK_CUDA_REC(cudaGraphLaunch(proj_graph_exec_, stream));
                } else {
                    // Capture failed — fall through to direct launch for this iteration
                    int blocks_rep = (u_words + threads - 1) / threads;
                    k_replicate_matrix<<<blocks_rep, threads, 0, stream>>>(
                        d_U_expanded_, d_U_small_, num_blocks, u_words);

                    matmul_gf2_gpu(n_block_, d_Z_, d_U_expanded_, d_MatMul_Res_, num_blocks, stream);

                    int blocks_xor = (total_w + threads - 1) / threads;
                    k_xor_accumulate<<<blocks_xor, threads, 0, stream>>>(
                        d_W_batch_, d_MatMul_Res_, total_w);
                }
            } else
#endif
#endif
            {
                // --- Direct Launch Path (fallback / non-graph) ---
                int blocks_rep = (u_words + threads - 1) / threads;
                k_replicate_matrix<<<blocks_rep, threads, 0, stream>>>(
                    d_U_expanded_, d_U_small_, num_blocks, u_words);

                matmul_gf2_gpu(n_block_, d_Z_, d_U_expanded_, d_MatMul_Res_, num_blocks, stream);

                int blocks_xor = (total_w + threads - 1) / threads;
                k_xor_accumulate<<<blocks_xor, threads, 0, stream>>>(
                    d_W_batch_, d_MatMul_Res_, total_w);
            }

            // C. Optional: Intermedia Verification of Horner Result (at exact degree)
	    // We do NOT strip here. We wait for the parallel RunBatchStripping phase.
            bool recording = config_.oracle_verification && oracle_data_.empty();
            bool verifying = !oracle_data_.empty();	    
	    if (recording || verifying) {
                for (int i = 0; i < batch.candidates.size(); ++i) {
                    if (batch.candidates[i].degree == k) {
		        int grid = (nrows_ + 63) / 64;
                        CHECK_CUDA_REC(cudaMemsetAsync(d_extracted_sol_, 0, total_bytes, stream));
                        k_extract_column<<<grid, 64, 0, stream>>>(d_extracted_sol_, d_W_batch_, i, nrows_, n_block_);

                        if (recording) {
			    uint64_t h = hash_device_vector(d_extracted_sol_, stream);
                            recorded_data_[batch.candidates[i].col_idx].hash_horner = h;
                        } 
                        // Only verify if we have data (avoids "missing" warning during recording)
                        if (verifying) {
                            VerifyAgainstOracle(batch.candidates[i].col_idx, d_extracted_sol_, nullptr, stream);
			}
		    }
                }
            }
        }

        // --- Parallel Valuation Stripping ---
        RunBatchStripping(batch, stream);

        if (!solution_buffer_.empty()) {
            out_solution = solution_buffer_.front();
            solution_buffer_.pop_front();
            return true;
        }
    }

    LOG(LOG_STATS) << "[SolutionReconstructor] Batch search exhausted.";
    return false;
}

void SolutionReconstructor::RunBatchStripping(const BatchCandidate& batch, cudaStream_t stream) {

    // Configure Loop Max Steps
    int max_steps;
    if (config_.stripping_step_limit > 0) {
        max_steps = config_.stripping_step_limit;
    } else {
        // Heuristic: If valuation stripping doesn't converge quickly, it's likely a spurious candidate.
        max_steps = (nrows_ / 1024 + 128 < 4096) ? nrows_ / 1024 + 128 : 4096;
    }  

    LOG(LOG_DEBUG_2) << "[SolutionReconstructor] Batch Valuation Stripping with max_steps = " << max_steps;
    
    size_t words_per_row = (n_block_ + 63) / 64;
    // Use aligned size for safe history snapshots
    size_t total_bytes = nrows_aligned_ * words_per_row * sizeof(uint64_t);
    int grid = (nrows_ + 63) / 64;
    
    // Status tracking
    std::vector<int> h_col_status(n_block_);
    // All candidates are active initially
    std::vector<bool> is_active(batch.candidates.size(), true);
    int active_count = batch.candidates.size();  

    if (active_count == 0) return;
    
    int cursor = 0;
    
    // Save initial state (step 0 of stripping)
    CHECK_CUDA_REC(cudaMemcpyAsync(d_history_ + cursor * (total_bytes/8), d_W_batch_, total_bytes, cudaMemcpyDeviceToDevice, stream));
    
    for (int step = 1; step <= max_steps; ++step) {
        
        // Advance cursor
        cursor = (cursor + 1) % history_depth_;
        
        // Compute Next Step: W = B * W
        CHECK_CUDA_REC(cudaMemsetAsync(d_Tmp_, 0, total_bytes, stream));
        op_.mul(d_Tmp_, d_W_batch_, stream);
        CHECK_CUDA_REC(cudaMemcpyAsync(d_W_batch_, d_Tmp_, total_bytes, cudaMemcpyDeviceToDevice, stream));

        // Save to History
        uint64_t* d_snap = d_history_ + cursor * (total_bytes/8);
        CHECK_CUDA_REC(cudaMemcpyAsync(d_snap, d_W_batch_, total_bytes, cudaMemcpyDeviceToDevice, stream));
        
        // Check Interval
        if (step % check_interval_ == 0) {
            // Check zeros
            k_check_column_zeros<<<n_block_, 256, 0, stream>>>(d_col_status_, d_W_batch_, nrows_, n_block_);
            CHECK_CUDA_REC(cudaMemcpyAsync(h_col_status.data(), d_col_status_, n_block_ * sizeof(int), cudaMemcpyDeviceToHost, stream));
            CHECK_CUDA_REC(cudaStreamSynchronize(stream));
            
            // for (int i = 0; i < batch.candidates.size(); ++i) {
            for (int i = batch.candidates.size() - 1; i >= 0; --i) {
                if (is_active[i] && h_col_status[i] == 1) {
                    // Column died since last check.
                    // Backtrack to find last non-zero.
                    // Use nrows_aligned_ for correct history stride
                    k_backtrack_column<<<1, 256, 0, stream>>>(
                        d_found_idx_, d_history_, i, cursor, history_depth_, nrows_aligned_, n_block_
                    );
                    int found_idx = -1;
                    CHECK_CUDA_REC(cudaMemcpyAsync(&found_idx, d_found_idx_, sizeof(int), cudaMemcpyDeviceToHost, stream));
                    CHECK_CUDA_REC(cudaStreamSynchronize(stream));
                    
                    if (found_idx != -1) {
                        // Extract solution from history
                        uint64_t* d_source = d_history_ + found_idx * (total_bytes/8);
                        CHECK_CUDA_REC(cudaMemsetAsync(d_extracted_sol_, 0, total_bytes, stream));
                        k_extract_column<<<grid, 64, 0, stream>>>(d_extracted_sol_, d_source, i, nrows_, n_block_);
                        
                        // Verify & Store
                        const auto& cand = batch.candidates[i];
                        
                        // Verification (Final)
                        if (config_.oracle_verification && oracle_data_.empty()) {
			    // Recording
                            uint64_t f = hash_device_vector(d_extracted_sol_, stream);
                            recorded_data_[cand.col_idx].hash_final = f;
                        }
                        if (!oracle_data_.empty()) {
                            VerifyAgainstOracle(cand.col_idx, nullptr, d_extracted_sol_, stream);
                        }

                        // Download: Use aligned buffer to prevent overflow
                        std::vector<uint64_t> hW(nrows_aligned_ * words_per_row);
                        CHECK_CUDA_REC(cudaMemcpyAsync(hW.data(), d_extracted_sol_, total_bytes, cudaMemcpyDeviceToHost, stream));
                        CHECK_CUDA_REC(cudaStreamSynchronize(stream));
                        
                        std::vector<uint64_t> w_packed((nrows_ + 63) / 64, 0);
                        int weight = 0;
                        for (int r = 0; r < nrows_; ++r) {
                            if (hW[r * words_per_row] & 1ULL) {
                                w_packed[r / 64] |= (1ULL << (r % 64));
                                weight++;
                            }
                        }
                        
                        if (weight > 0) {
                            if (insert_into_basis(basis_struct_, w_packed)) {
                                LOG(LOG_DEBUG_1) << "[SolutionReconstructor] Found Independent Solution #" 
                                              << basis_struct_.size() << " (Weight: " << weight 
                                              << ", Source: Col " << cand.col_idx << ")";
                                solution_buffer_.push_back(basis_struct_.back().vec);
                            }
                        }
                    } else {
                        LOG(LOG_DEBUG_2) << "[SolutionReconstructor] Backtrack failed for col " << i 
                                         << ". History depth " << history_depth_ << " exceeded?";
                    }
                    
                    is_active[i] = false;
                    active_count--;
                }
            }
            if (active_count == 0) break;
        }
    }
}

