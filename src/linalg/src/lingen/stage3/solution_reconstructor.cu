void SolutionReconstructor::Init(const std::vector<uint64_t>& hY,
                                 const std::vector<uint64_t>& hPi,
                                 cudaStream_t stream) 
{
    LOG(LOG_STATS) << "[SolutionReconstructor] Initializing Search State...";
    ptr_hPi_ = &hPi;
    M_ = m_block_ + n_block_;

    size_t words_per_row = (n_block_ + 63) / 64;
    size_t valid_bytes = nrows_ * words_per_row * sizeof(uint64_t);
    size_t alloc_bytes = nrows_aligned_ * words_per_row * sizeof(uint64_t);
    
    // Zero out Z completely (including padding area)
    CHECK_CUDA_REC(cudaMemsetAsync(d_Z_, 0, alloc_bytes, stream));
    // Copy only the valid rows from host
    CHECK_CUDA_REC(cudaMemcpyAsync(d_Z_, hY.data(), valid_bytes, cudaMemcpyHostToDevice, stream));

    size_t mat_stride = M_ * ((M_ + 63) / 64);
    if (mat_stride == 0) return; 
    int degree_len = (int)(hPi.size() / mat_stride);
    
    candidate_queue_.clear();
    batch_queue_.clear();
    solution_buffer_.clear();
    basis_struct_.clear();
    queue_cursor_ = 0;

    LOG_INCREMENT_STAGE(10);
    LOG(LOG_DEBUG_2) << "[SolutionReconstructor] [Init] Extracting degrees...";
    for (int r = 0; r < M_; ++r) {
        int deg = -1;
        for (int k = degree_len - 1; k >= 0; --k) {
            auto u = extract_coeff_vector(hPi, M_, n_block_, k, r);
            bool nz = false;
            for(auto x : u) if(x) nz = true;
            if (nz) { deg = k; break; }
        }
        if (deg != -1) candidate_queue_.push_back({deg, r});
    }

    std::sort(candidate_queue_.begin(), candidate_queue_.end());

    if (config_.batch_mode) {
        LOG(LOG_DEBUG_2) << "[SolutionReconstructor] Building batches...";
        BatchCandidate current_batch;
        current_batch.max_degree = -1;
        for (const auto& cand : candidate_queue_) {
            current_batch.candidates.push_back(cand);
            current_batch.max_degree = std::max(current_batch.max_degree, cand.degree);
            if (current_batch.candidates.size() >= (uint64_t)n_block_) {
                batch_queue_.push_back(current_batch);
                current_batch = BatchCandidate();
                current_batch.max_degree = -1;
            }
        }
        if (!current_batch.candidates.empty()) batch_queue_.push_back(current_batch);
        LOG(LOG_STATS) << "[SolutionReconstructor] Batch Mode: Created " << batch_queue_.size() << " batches.";
    } else {
        LOG(LOG_STATS) << "[SolutionReconstructor] Legacy Mode: " << candidate_queue_.size() << " candidates queued.";
    }
}

bool SolutionReconstructor::FindNext(std::vector<uint64_t>& out_solution, cudaStream_t stream) {
    if (config_.batch_mode) return FindNextBatch(out_solution, stream);
    else return FindNextLegacy(out_solution, stream);
}

