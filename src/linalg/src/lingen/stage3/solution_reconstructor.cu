void SolutionReconstructor::construct_batch_matrix(
    std::vector<uint64_t>& out_U, 
    int n_block, 
    const std::vector<uint64_t>& hPi, 
    int M, 
    int k, 
    const std::vector<Candidate>& batch
) {
    size_t words_per_row = (n_block + 63) / 64;
    std::fill(out_U.begin(), out_U.end(), 0);
    for (size_t col = 0; col < batch.size(); ++col) {
        const auto& cand = batch[col];
        if (k > cand.degree) continue; 
        auto u_vec = extract_coeff_vector(hPi, M, n_block, k, cand.col_idx);
        for (int row = 0; row < n_block; ++row) {
            uint64_t bit = (u_vec[row / 64] >> (row % 64)) & 1ULL;
            if (bit) out_U[row * words_per_row + (col / 64)] |= (1ULL << (col % 64));
        }
    }
}

