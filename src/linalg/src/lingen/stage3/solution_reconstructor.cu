bool SolutionReconstructor::FindNextLegacy(std::vector<uint64_t>& out_solution, cudaStream_t stream) {
    if (!ptr_hPi_) return false;
    while (queue_cursor_ < candidate_queue_.size()) {
        const auto& cand = candidate_queue_[queue_cursor_];
        queue_cursor_++;
        if (ProcessCandidate(cand, out_solution, stream)) return true;
    }
    return false;
}

