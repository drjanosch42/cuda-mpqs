__global__ void k_add_mul_Z_u_general(
    uint64_t* __restrict__ dDest, 
    const uint64_t* __restrict__ dZ, 
    const uint64_t* __restrict__ d_u, 
    int nrows, 
    int n_block
) {
    int row = blockIdx.x * blockDim.x + threadIdx.x;
    if (row >= nrows) return;
    int words_per_row = (n_block + 63) / 64;
    bool dot = false;
    for (int w = 0; w < words_per_row; ++w) {
        uint64_t z_word = dZ[row * words_per_row + w];
        uint64_t u_word = d_u[w];
        dot ^= (__popcll(z_word & u_word) & 1);
    }
    if (dot) dDest[row * words_per_row] ^= 1ULL; 
}

__global__ void k_replicate_matrix(
    uint64_t* __restrict__ dst,
    const uint64_t* __restrict__ src,
    int num_blocks,
    int mat_size_words
) {
    int idx = blockIdx.x * blockDim.x + threadIdx.x;
    if (idx >= mat_size_words) return;
    uint64_t val = src[idx];
    for(int b = 0; b < num_blocks; ++b) dst[b * mat_size_words + idx] = val;
}

__global__ void k_xor_accumulate(
    uint64_t* __restrict__ dst,
    const uint64_t* __restrict__ src,
    size_t total_words
) {
    size_t idx = blockIdx.x * blockDim.x + threadIdx.x;
    if (idx < total_words) dst[idx] ^= src[idx];
}

__global__ void k_extract_column(
    uint64_t* __restrict__ d_vec,
    const uint64_t* __restrict__ d_batch,
    int col_idx,
    int nrows,
    int n_block
) {
    int row = blockIdx.x * blockDim.x + threadIdx.x;
    if (row >= nrows) return;
    int words_per_row = (n_block + 63) / 64;
    uint64_t word = d_batch[row * words_per_row + (col_idx / 64)];
    uint64_t bit = (word >> (col_idx % 64)) & 1ULL;
    if (bit) d_vec[row * words_per_row] |= 1ULL;
    else d_vec[row * words_per_row] &= ~1ULL;
}

__global__ void k_check_column_zeros(
    int* __restrict__ d_col_status,
    const uint64_t* __restrict__ d_batch,
    int nrows,
    int n_block
) {
    int col = blockIdx.x; 
    if (col >= n_block) return;
    
    int words_per_row = (n_block + 63) / 64;
    int tid = threadIdx.x;
    int stride = blockDim.x;
    
    int local_is_zero = 1;
    for (int r = tid; r < nrows; r += stride) {
        uint64_t word = d_batch[r * words_per_row + (col / 64)];
        if ((word >> (col % 64)) & 1ULL) {
            local_is_zero = 0;
            break;
        }
    }
    
    int has_bit = !local_is_zero;
    for (int offset = 16; offset > 0; offset /= 2) 
        has_bit |= __shfl_down_sync(0xFFFFFFFF, has_bit, offset);
        
    __shared__ int shm[32]; 
    if ((tid % 32) == 0) shm[tid / 32] = has_bit;
    __syncthreads();
    
    if (tid < 32) {
        int val = (tid < (blockDim.x + 31)/32) ? shm[tid] : 0;
        for (int offset = 16; offset > 0; offset /= 2)
            val |= __shfl_down_sync(0xFFFFFFFF, val, offset);
        if (tid == 0) d_col_status[col] = (val == 0) ? 1 : 0;
    }
}

__global__ void k_backtrack_column(
    int* __restrict__ d_found_idx,
    const uint64_t* __restrict__ d_history,
    int col,
    int cursor,
    int depth,
    int nrows,
    int n_block
) {
    int tid = threadIdx.x;
    int words_per_row = (n_block + 63) / 64;
    size_t batch_size_words = (size_t)nrows * words_per_row;
    
    if (tid == 0) *d_found_idx = -1;
    __syncthreads();

    for (int back = 1; back < depth; ++back) {
        int hist_idx = (cursor - back + depth) % depth;
        const uint64_t* d_snapshot = d_history + hist_idx * batch_size_words;
        
        int has_bit = 0;
        for (int r = tid; r < nrows; r += blockDim.x) {
            uint64_t word = d_snapshot[r * words_per_row + (col / 64)];
            if ((word >> (col % 64)) & 1ULL) {
                has_bit = 1;
                break;
            }
        }
        
        for (int offset = 16; offset > 0; offset /= 2) 
            has_bit |= __shfl_down_sync(0xFFFFFFFF, has_bit, offset);
            
        __shared__ int block_has_bit;
        if (tid == 0) block_has_bit = 0;
        __syncthreads();
        
        if ((tid % 32) == 0 && has_bit) atomicOr(&block_has_bit, 1);
        __syncthreads();
        
        if (block_has_bit) {
            if (tid == 0) *d_found_idx = hist_idx;
            return;
        }
    }
}

