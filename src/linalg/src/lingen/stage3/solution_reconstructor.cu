bool SolutionReconstructor::ProcessCandidate(const Candidate& cand, std::vector<uint64_t>& out_solution, cudaStream_t stream) {
    size_t words_per_row = (n_block_ + 63) / 64;
    // Use aligned size even in legacy to ensure buffer safety
    size_t total_bytes = nrows_aligned_ * words_per_row * sizeof(uint64_t);
    int grid = (nrows_ + 63) / 64;

    LOG(LOG_DEBUG_2) << "[SolutionReconstructor] Processing Candidate Col=" << cand.col_idx 
                     << " Deg=" << cand.degree;

    CHECK_CUDA_REC(cudaMemsetAsync(d_W_acc_, 0, total_bytes, stream));
    for (int k = 0; k <= cand.degree; ++k) {
        if (k > 0) {
            CHECK_CUDA_REC(cudaMemsetAsync(d_Tmp_, 0, total_bytes, stream));
            op_.mul(d_Tmp_, d_W_acc_, stream);
            CHECK_CUDA_REC(cudaMemcpyAsync(d_W_acc_, d_Tmp_, total_bytes, cudaMemcpyDeviceToDevice, stream));
        }
        auto u_vec = extract_coeff_vector(*ptr_hPi_, M_, n_block_, k, cand.col_idx);
        bool u_nz = false; 
        for(auto x : u_vec) if(x) u_nz=true;
        if (u_nz) {
            CHECK_CUDA_REC(cudaMemcpy(d_u_vec_, u_vec.data(), u_vec.size()*sizeof(uint64_t), cudaMemcpyHostToDevice));
            k_add_mul_Z_u_general<<<grid, 64, 0, stream>>>(d_W_acc_, d_Z_, d_u_vec_, nrows_, n_block_);
        }
    }

    if (ProcessFoundSolution(cand, d_W_acc_, stream)) {
        out_solution = basis_struct_.back().vec;
        return true;
    }
    return false;
}

bool SolutionReconstructor::ProcessFoundSolution(const Candidate& cand, uint64_t* d_sol_vec, cudaStream_t stream) {
    bool recording = config_.oracle_verification && oracle_data_.empty();
    bool verifying = !oracle_data_.empty();
    
    if (recording) {
        uint64_t h_hash = hash_device_vector(d_sol_vec, stream);
        recorded_data_[cand.col_idx].hash_horner = h_hash;
    }
    if (verifying) {
        VerifyAgainstOracle(cand.col_idx, d_sol_vec, nullptr, stream);
    }
    if (is_zero_vector(d_sol_vec, stream)) return false;
    int limit = (config_.stripping_step_limit > 0) ? config_.stripping_step_limit : (nrows_ + 128);
    bool converged = strip_valuation(d_sol_vec, d_Tmp_, limit, stream);
    if (converged) {
        if (recording) {
            uint64_t f_hash = hash_device_vector(d_sol_vec, stream);
            recorded_data_[cand.col_idx].hash_final = f_hash;
        }
        if (verifying) {
            VerifyAgainstOracle(cand.col_idx, nullptr, d_sol_vec, stream);
        }
        
        size_t words_per_row = (n_block_ + 63) / 64;
        // Use aligned size for safe download
        std::vector<uint64_t> hW(nrows_aligned_ * words_per_row);
        size_t total_bytes = nrows_aligned_ * words_per_row * sizeof(uint64_t);
        
        CHECK_CUDA_REC(cudaMemcpyAsync(hW.data(), d_sol_vec, total_bytes, cudaMemcpyDeviceToHost, stream));
        CHECK_CUDA_REC(cudaStreamSynchronize(stream));
        std::vector<uint64_t> w_packed((nrows_ + 63) / 64, 0);
        int weight = 0;
        for (int r = 0; r < nrows_; ++r) {
            if (hW[r * words_per_row] & 1ULL) {
                w_packed[r / 64] |= (1ULL << (r % 64));
                weight++;
            }
        }
        if (weight > 0 && insert_into_basis(basis_struct_, w_packed)) {
            LOG(LOG_INFO) << "[SolutionReconstructor] Found Independent Solution #" 
                          << basis_struct_.size() << " (Weight: " << weight << ", Source: Col " << cand.col_idx << ")";
            return true; 
        }
    }
    return false;
}

// -----------------------------------------------------------------------------
// Core Implementation Helpers
// -----------------------------------------------------------------------------
bool SolutionReconstructor::insert_into_basis(std::vector<BasisVector>& basis, std::vector<uint64_t>& vec) {
    bool changed = true;
    while(changed) {
        changed = false;
        int pivot = -1;
        for(size_t i=0; i<vec.size(); ++i) {
            if (vec[i] != 0) {
                pivot = i * 64 + __builtin_ctzll(vec[i]);
                break;
            }
        }
        if (pivot == -1) return false; 
        for(const auto& b : basis) {
            if (b.pivot == pivot) {
                for(size_t k=0; k<vec.size(); ++k) vec[k] ^= b.vec[k];
                changed = true;
                break;
            }
        }
    }
    int pivot = -1;
    for(size_t i=0; i<vec.size(); ++i) {
        if (vec[i] != 0) {
            pivot = i * 64 + __builtin_ctzll(vec[i]);
            break;
        }
    }
    if (pivot != -1) {
        basis.push_back({pivot, vec});
        return true;
    }
    return false;
}

