SolutionReconstructor::SolutionReconstructor(
    const lingen::BwOperator& op, const BWStage3Config& config
) : op_(op), config_(config)
{
    // Initialize convenience members from config
    nrows_ = config_.nrows;
    m_block_ = config_.m_block;
    n_block_ = config_.n_block;
    
    // Initialize tuning parameters
    history_depth_ = config_.history_depth;
    check_interval_ = config_.check_interval;
    
    // Align nrows to n_block boundary for block-based MatMul
    nrows_aligned_ = (nrows_ + n_block_ - 1) / n_block_ * n_block_;
    
    size_t words_per_row = (n_block_ + 63) / 64;
    size_t total_bytes = nrows_aligned_ * words_per_row * sizeof(uint64_t);  

    d_Z_ = nullptr;
    d_W_acc_ = nullptr;
    d_W_batch_ = nullptr;
    d_Tmp_ = nullptr;
    d_u_vec_ = nullptr;
    d_U_small_ = nullptr;
    d_U_expanded_ = nullptr;
    d_MatMul_Res_ = nullptr;
    d_extracted_sol_ = nullptr;
    d_history_ = nullptr;
    d_col_status_ = nullptr;
    d_found_idx_ = nullptr;

    CHECK_CUDA_REC(cudaMalloc(&d_Z_, total_bytes));
    CHECK_CUDA_REC(cudaMalloc(&d_W_acc_, total_bytes));
    CHECK_CUDA_REC(cudaMalloc(&d_Tmp_, total_bytes));
    CHECK_CUDA_REC(cudaMalloc(&d_u_vec_, words_per_row * sizeof(uint64_t)));
    
    CHECK_CUDA_REC(cudaMalloc(&d_W_batch_, total_bytes));
    CHECK_CUDA_REC(cudaMalloc(&d_MatMul_Res_, total_bytes));
    CHECK_CUDA_REC(cudaMalloc(&d_U_expanded_, total_bytes)); 
    CHECK_CUDA_REC(cudaMalloc(&d_extracted_sol_, total_bytes));

    size_t u_small_bytes = n_block_ * ((n_block_ + 63) / 64) * sizeof(uint64_t);
    CHECK_CUDA_REC(cudaMalloc(&d_U_small_, u_small_bytes));
    
    CHECK_CUDA_REC(cudaMalloc(&d_history_, total_bytes * history_depth_));
    CHECK_CUDA_REC(cudaMalloc(&d_col_status_, n_block_ * sizeof(int)));
    CHECK_CUDA_REC(cudaMalloc(&d_found_idx_, sizeof(int)));

    CHECK_CUDA_REC(cudaMemset(d_Z_, 0, total_bytes));
    CHECK_CUDA_REC(cudaMemset(d_W_acc_, 0, total_bytes));
    CHECK_CUDA_REC(cudaMemset(d_W_batch_, 0, total_bytes));
    
    // Resize host buffer to aligned size to prevent overflow
    h_chk_buf_.resize(nrows_aligned_ * words_per_row);
}
 
